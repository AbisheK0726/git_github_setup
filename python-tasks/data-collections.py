# Lists []

shopping_list = ["milk", "pasta", "eggs", "spam", "bread", "rice"]

shopping_list.append("cookies") # Add to the end of the list
print(shopping_list)

shopping_list.remove("spam") # Remove from the list
print(shopping_list)

shopping_list.pop(4) # Remove from the list
print(shopping_list)

# Tuples ()

city = ("New York", "London", "Paris", "Berlin", "Tokyo", "Moscow") # Tuples are immutable
print(city[0])
print(type(city))
